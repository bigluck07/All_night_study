
public class WhileTest {

	public static void main(String[] args) {
		
		//2. 반복문
		/*
		 * 구성요소
		 * - 시작값: 변수 선언
		 * - 조건식: 비교 / 논리연산자
		 * - 증감식: 증감연산자
		 */
		int n = 1;
		while (n <= 5) {
			System.out.println("hello");
			n++;
		}//end while
		
		System.out.println("end");
		
	}//end main

}//end class
