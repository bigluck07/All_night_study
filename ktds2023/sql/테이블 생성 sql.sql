CREATE TABLE IF NOT EXISTS DEPT
 (DEPTNO INT  PRIMARY KEY,
	DNAME VARCHAR(14) ,
	LOC VARCHAR(13) ) ;

CREATE TABLE emp
 (EMPNO INT,
	ENAME VARCHAR(10),
	JOB VARCHAR(9),
	MGR INT,
	HIREDATE DATE,
	SAL FLOAT,
	COMM INT,
	DEPTNO INT,
	PRIMARY KEY(EMPNO),
	FOREIGN KEY (DEPTNO)  REFERENCES DEPT(DEPTNO) );

