package com.fastcampus.de.java.clip9;

public class IfStatment {
    public static void main(String[] args) {
        int check = 100;
        int num1 = 90;
        if (num1 > check) {
            System.out.println("over 100");
        } else if (num1 == check){
            System.out.println("is 100");
        } else {
            System.out.println("under 100");
        }
    }
}
